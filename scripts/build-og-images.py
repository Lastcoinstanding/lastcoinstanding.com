#!/usr/bin/env python3
"""
Build the product-forward OG cards (STYLE_GUIDE §6.15.2).

Visits each page in headless Chromium, captures its hero visual as a PNG
(Playwright screenshot on the live DOM element — works on canvases too),
embeds that PNG as an <img> in a programmatically-injected OG frame, then
screenshots the frame at 2x device-scale and downsamples to 1280×720.

Cards produced (all written to the output directory, default = cwd):
    og-heatmap.jpg
    og-bitcoin-vs-the-stock-market.jpg
    og-the-bitcoin-retirement.jpg
    og-calculators.jpg

When to run
-----------
After each monthly Power Law / TODAY data refresh — the embedded chart
screenshots go stale otherwise. See MONTHLY_REFRESH_CHECKLIST.md §6.

The brand-forward family (§6.15.1 — Melting Ice Cube, Power Law, WMHTB,
etc.) does NOT need this script; those cards composite static atmospheric
artwork with no data dependency.

The homepage (og-image.jpg) was migrated from product-forward to
brand-forward on 2026-05-17 (commit 458a9fc041). It is intentionally not
regenerated by this script.

Usage
-----
    python3 scripts/build-og-images.py                  # writes to ./
    python3 scripts/build-og-images.py --output-dir og/ # writes to og/
    python3 scripts/build-og-images.py --only heatmap   # build one card

Or via the package.json shortcut:
    npm run build-ogs

Dependencies
------------
    pip install playwright pillow
    playwright install chromium

After regeneration
------------------
Verify each card serves from prod:
    curl -I https://lastcoinstanding.com/og-heatmap.jpg
must return `Content-Type: image/jpeg`. Then poke Facebook's debugger
(developers.facebook.com/tools/debug/) and post a draft tweet to force
re-scrape on the social platforms.
"""

