def capture_hero_png(page, selector: str, wait_after_scroll_ms: int = 0) -> bytes:
    """Take a Playwright screenshot of a DOM element, return PNG bytes.
    Scrolls into view first; optional post-scroll wait lets intersection-
    observer-triggered renders (Chart.js, mini-tiles) finish painting.
    """
    el = page.locator(selector)
    el.scroll_into_view_if_needed()
    if wait_after_scroll_ms:
        page.wait_for_timeout(wait_after_scroll_ms)
    return el.screenshot(type="png")


def png_to_data_url(png_bytes: bytes) -> str:
    return "data:image/png;base64," + base64.b64encode(png_bytes).decode()


def build_card(page, card_config: dict, output_path: Path) -> None:
    """Build one OG card. Writes JPEG to output_path."""
    started = time.time()
    print(f"  visit {card_config['url']}")
    page.goto(card_config["url"], wait_until="networkidle")
    try:
        page.wait_for_selector(
            card_config["hero_selector"], state="attached", timeout=10_000)
    except PWTimeout:
        raise RuntimeError(
            f"hero selector not found on page: {card_config['hero_selector']}")
    page.wait_for_timeout(card_config["wait_after_navigate_ms"])

    # Optional: click an element before capture (e.g. a tab button to reveal
    # the hero chart on a tabbed page). The chart panel must become visible
    # before scroll_into_view_if_needed will succeed.
    click_selector = card_config.get("click_before_capture")
    if click_selector:
        print(f"  click {click_selector}")
        page.click(click_selector)
        page.wait_for_timeout(900)  # tab transition + chart render

    # 1. Capture hero element as a PNG
    hero_png = capture_hero_png(
        page,
        card_config["hero_selector"],
        wait_after_scroll_ms=card_config.get("wait_after_scroll_ms", 0),
    )
    print(f"  hero PNG: {len(hero_png):,} bytes")
    hero_data_url = png_to_data_url(hero_png)

    # 2. Inject OG frame with the hero embedded as <img>
    chrome = dict(card_config["chrome"])
    chrome["heroDataUrl"] = hero_data_url
    page.evaluate(OG_INJECT_JS, chrome)
    page.wait_for_timeout(300)

    # 3. Screenshot the frame (2x device-scale gives 2560x1440)
    frame = page.locator("#og-frame")
    png_bytes = frame.screenshot(type="png")

    # 4. Downsample → 1280x720 and save as JPEG quality 82
    img = Image.open(io.BytesIO(png_bytes))
    if img.size != (1280, 720):
        img = img.resize((1280, 720), Image.LANCZOS)
    img = img.convert("RGB")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    img.save(output_path, "JPEG", quality=82, optimize=True, progressive=False)
    elapsed = time.time() - started
    print(f"  wrote {output_path} ({output_path.stat().st_size:,} bytes, "
          f"{elapsed:.1f}s)")


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__.split("\n")[1])
    ap.add_argument(
        "--output-dir", default=".",
        help="Directory to write the JPEG files into (default: current directory)")
    ap.add_argument(
        "--only", action="append", choices=[c["name"] for c in CARDS],
        help="Build only the named card (can be passed multiple times)")
    args = ap.parse_args()

    output_dir = Path(args.output_dir).resolve()
    cards = CARDS if not args.only else [c for c in CARDS if c["name"] in args.only]

    print(f"Building {len(cards)} card(s) to {output_dir}")

    failures = []
    with sync_playwright() as p:
        browser = p.chromium.launch(headless=True)
        context = browser.new_context(
            viewport={"width": 1280, "height": 900},
            device_scale_factor=2,
        )
        page = context.new_page()

        for cfg in cards:
            print(f"\n[{cfg['name']}] {cfg['output_filename']}")
            try:
                build_card(page, cfg, output_dir / cfg["output_filename"])
            except Exception as e:
                print(f"  FAILED: {type(e).__name__}: {e}")
                failures.append((cfg["name"], str(e)))

        browser.close()

    if failures:
        print(f"\n{len(failures)} card(s) failed:")
        for name, err in failures:
            print(f"  {name}: {err}")
        return 1
    print(f"\nAll {len(cards)} card(s) built successfully.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
